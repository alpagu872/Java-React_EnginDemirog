public class F_SwitchBloklari {
    public static void main(String[] args) {
        char secim = 'B';
        switch (secim){
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
            case 'C':
                System.out.println("C");
                break;
        }
    }
}
